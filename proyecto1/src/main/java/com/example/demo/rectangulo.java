package com.example.demo;

public class rectangulo {
     
	int alt;
	int base;
	
	public int superficie(int base, int alt) {
		return base*alt;
	}

	public int getAlt() {
		return alt;
	}

	public void setAlt(int alt) {
		this.alt = alt;
	}

	public int getBase() {
		return base;
	}

	public void setBase(int base) {
		this.base = base;
	}
	
	
}
